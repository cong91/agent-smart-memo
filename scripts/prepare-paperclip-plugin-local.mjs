import { cpSync, existsSync, mkdirSync, readFileSync, rmSync, writeFileSync } from "node:fs";
import { join } from "node:path";
import { pathToFileURL } from "node:url";

const root = process.cwd();
const pluginDist = join(root, "packages", "plugins", "agent-smart-memo", "dist");
const outDir = join(root, "artifacts", "paperclip-plugin-local");

if (!existsSync(pluginDist)) {
  console.error(`[prepare-paperclip-plugin-local] Missing plugin dist at: ${pluginDist}`);
  console.error("Run plugin build first (or ensure dist artifacts exist).");
  process.exit(1);
}

rmSync(outDir, { recursive: true, force: true });
mkdirSync(outDir, { recursive: true });

cpSync(pluginDist, join(outDir, "dist"), { recursive: true });

const rootPkg = JSON.parse(readFileSync(join(root, "package.json"), "utf8"));

const hostWorkerPath = join(outDir, "host-worker.mjs");

const pkg = {
  name: "@paperclip/plugin-asm-memory-local",
  version: rootPkg.version,
  private: true,
  type: "module",
  description: "Local-install bundle for ASM Paperclip plugin smoke/debug",
  main: "./dist/worker.js",
  exports: {
    ".": "./dist/worker.js",
    "./manifest": "./dist/manifest.js",
    "./worker": "./host-worker.mjs",
    "./types": "./dist/types.js"
  },
  files: [
    "README.local.md",
    "dist/",
    "host-worker.mjs",
    "plugin.manifest.json"
  ],
  paperclipPlugin: {
    manifest: "./dist/manifest.js",
    worker: "./host-worker.mjs"
  }
};

writeFileSync(join(outDir, "package.json"), `${JSON.stringify(pkg, null, 2)}\n`, "utf8");

const hostWorker = `import { createAsmMemoryWorker } from "./dist/worker.js";

const worker = createAsmMemoryWorker();

export function initialize(input) {
  return worker.initialize(input);
}

export function health() {
  return worker.health();
}

export async function shutdown() {
  return { ok: true };
}

export function executeTool(toolName, payload) {
  return worker.executeTool(toolName, payload);
}

export function onEvent(eventName, payload) {
  return worker.onEvent(eventName, payload);
}

export function runJob(jobName, payload) {
  return worker.runJob(jobName, payload);
}

export function getData(key, params) {
  return worker.getData(key, params);
}

export function performAction(action, payload) {
  return worker.performAction(action, payload);
}
`;
writeFileSync(hostWorkerPath, hostWorker, "utf8");

const manifestModule = await import(pathToFileURL(join(outDir, "dist", "manifest.js")).href);
const manifest = manifestModule?.manifest || {};
writeFileSync(join(outDir, "plugin.manifest.json"), `${JSON.stringify(manifest, null, 2)}\n`, "utf8");

const readme = `# ASM Paperclip Plugin Local Bundle\n\nGenerated by: \`node scripts/prepare-paperclip-plugin-local.mjs\`\n\n## Install target\n- Path: \`${outDir}\`\n- Intended use: local install/debug on Paperclip host via \`paperclipai plugin install <local-path>\`\n\n## Host loader contract (out-of-box)\n- package.json includes \`paperclipPlugin.manifest\` and \`paperclipPlugin.worker\`\n- worker entry \`host-worker.mjs\` exports top-level lifecycle methods (\`initialize\`, \`health\`, \`shutdown\`)\n\n## Contents\n- \`dist/manifest.js\`\n- \`dist/worker.js\`\n- \`host-worker.mjs\`\n- \`plugin.manifest.json\` (materialized manifest for inspection)\n\n> Note: This is a local debug artifact, not a published npm package.\n`;
writeFileSync(join(outDir, "README.local.md"), readme, "utf8");

console.log(`[prepare-paperclip-plugin-local] Prepared local plugin bundle at: ${outDir}`);
