import { cpSync, existsSync, mkdirSync, readFileSync, rmSync, writeFileSync } from "node:fs";
import { join } from "node:path";
import { pathToFileURL } from "node:url";

const root = process.cwd();
const pluginDist = join(root, "packages", "plugins", "agent-smart-memo", "dist");
const outDir = join(root, "artifacts", "paperclip-plugin-local");

if (!existsSync(pluginDist)) {
  console.error(`[prepare-paperclip-plugin-local] Missing plugin dist at: ${pluginDist}`);
  console.error("Run plugin build first (or ensure dist artifacts exist).");
  process.exit(1);
}

rmSync(outDir, { recursive: true, force: true });
mkdirSync(outDir, { recursive: true });

cpSync(pluginDist, join(outDir, "dist"), { recursive: true });

const rootPkg = JSON.parse(readFileSync(join(root, "package.json"), "utf8"));

const pkg = {
  name: "@paperclip/plugin-asm-memory-local",
  version: rootPkg.version,
  private: true,
  type: "module",
  description: "Local-install bundle for ASM Paperclip plugin smoke/debug",
  main: "./dist/worker.js",
  exports: {
    ".": "./dist/worker.js",
    "./manifest": "./dist/manifest.js",
    "./worker": "./dist/worker.js",
    "./types": "./dist/types.js"
  },
  files: [
    "dist/",
    "plugin.manifest.json",
    "README.local.md"
  ]
};

writeFileSync(join(outDir, "package.json"), `${JSON.stringify(pkg, null, 2)}\n`, "utf8");

const manifestModule = await import(pathToFileURL(join(outDir, "dist", "manifest.js")).href);
const manifest = manifestModule?.manifest || {};
writeFileSync(join(outDir, "plugin.manifest.json"), `${JSON.stringify(manifest, null, 2)}\n`, "utf8");

const readme = `# ASM Paperclip Plugin Local Bundle\n\nGenerated by: \`node scripts/prepare-paperclip-plugin-local.mjs\`\n\n## Install target\n- Path: \`${outDir}\`\n- Intended use: local install/debug on Paperclip host via \`paperclipai plugin install <local-path>\`\n\n## Contents\n- \`dist/manifest.js\`\n- \`dist/worker.js\`\n- \`plugin.manifest.json\` (materialized manifest for inspection)\n\n> Note: This is a local debug artifact, not a published npm package.\n`;
writeFileSync(join(outDir, "README.local.md"), readme, "utf8");

console.log(`[prepare-paperclip-plugin-local] Prepared local plugin bundle at: ${outDir}`);
