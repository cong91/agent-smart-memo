import { cpSync, existsSync, mkdirSync, readFileSync, rmSync, writeFileSync } from "node:fs";
import { join } from "node:path";
import { pathToFileURL } from "node:url";

const root = process.cwd();
const pluginDist = join(root, "dist-paperclip");
const outDir = join(root, "artifacts", "paperclip-plugin-local");
const openclawPluginJsonPath = join(root, "openclaw.plugin.json");

if (!existsSync(pluginDist)) {
  console.error(`[prepare-paperclip-plugin-local] Missing paperclip dist at: ${pluginDist}`);
  console.error("Run npm run build:paperclip first.");
  process.exit(1);
}

if (!existsSync(openclawPluginJsonPath)) {
  console.error(`[prepare-paperclip-plugin-local] Missing OpenClaw plugin definition at: ${openclawPluginJsonPath}`);
  process.exit(1);
}

rmSync(outDir, { recursive: true, force: true });
mkdirSync(outDir, { recursive: true });

cpSync(pluginDist, join(outDir, "dist"), { recursive: true });
cpSync(openclawPluginJsonPath, join(outDir, "openclaw.plugin.json"));

const rootPkg = JSON.parse(readFileSync(join(root, "package.json"), "utf8"));

const pkg = {
  name: "@paperclip/plugin-asm-memory-local",
  version: rootPkg.version,
  private: true,
  type: "module",
  description: "Local-install bundle for ASM Paperclip plugin smoke/debug",
  main: "./dist/entries/paperclip.js",
  exports: {
    ".": "./dist/entries/paperclip.js",
    "./manifest": "./dist/entries/paperclip.js",
    "./worker": "./dist/entries/paperclip.js"
  },
  files: [
    "README.local.md",
    "dist/",
    "openclaw.plugin.json",
    "plugin.manifest.json"
  ],
  paperclipPlugin: {
    manifest: "./dist/entries/paperclip.js",
    worker: "./dist/entries/paperclip.js"
  }
};

writeFileSync(join(outDir, "package.json"), `${JSON.stringify(pkg, null, 2)}\n`, "utf8");

const paperclipEntryModule = await import(pathToFileURL(join(outDir, "dist", "entries", "paperclip.js")).href);
const manifest = paperclipEntryModule?.manifest || {};
writeFileSync(join(outDir, "plugin.manifest.json"), `${JSON.stringify(manifest, null, 2)}\n`, "utf8");

const readme = `# ASM Paperclip Plugin Local Bundle\n\nGenerated by: \`node scripts/prepare-paperclip-plugin-local.mjs\`\n\n## Install target\n- Path: \`${outDir}\`\n- Intended use: local install/debug on Paperclip host via \`paperclipai plugin install <local-path>\`\n\n## Host loader contract (out-of-box)\n- package.json includes \`paperclipPlugin.manifest\` and \`paperclipPlugin.worker\`\n- worker + manifest both resolve directly to source-built \`dist/entries/paperclip.js\`\n\n## Contents\n- \`dist/entries/paperclip.js\`\n- \`plugin.manifest.json\` (materialized manifest for inspection)\n\n> Note: This is a local install artifact generated from source build output.\n`;
writeFileSync(join(outDir, "README.local.md"), readme, "utf8");

console.log(`[prepare-paperclip-plugin-local] Prepared local plugin bundle at: ${outDir}`);
